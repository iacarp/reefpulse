const LANG_NAMES = { en: 'English', ro: 'Română', fr: 'Français', es: 'Español', de: 'Deutsch' };

const I18nContext = createContext({ t: T.en, lang: 'en', setLang: () => {}, langNames: LANG_NAMES });

export function I18nProvider({ children }) {
  const [lang, setLangState] = useState('en');

  useEffect(() => {
    AsyncStorage.getItem('reefpulse_lang').then(l => { if (l && T[l]) setLangState(l); });
  }, []);

  const setLang = async (l) => {
    setLangState(l);
    await AsyncStorage.setItem('reefpulse_lang', l);
  };

  return (
    <I18nContext.Provider value={{ t: T[lang] || T.en, lang, setLang, langNames: LANG_NAMES, allTranslations: T }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() { return useContext(I18nContext); }
export default { I18nProvider, useI18n };
